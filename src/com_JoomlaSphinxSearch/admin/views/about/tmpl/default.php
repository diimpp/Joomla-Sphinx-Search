Joomla Sphinx search component enables the Sphinx-powered search with high-performance and more relevant search results.

<h4>Features:</h4>
<ul>
    <li>Google-like search syntax;</li>
    <li>High searching performance;</li>
    <li>Improved search result relevance;</li>
    <li>Supports friendly URLs SEO settings;</li>
    <li>Supports morphological word forms dictionaries;</li>
    <li>Supports stemming (English, Russian and Czech are built-in);</li>
    <li>Supports stopwords;</li>
    <li>Supports both single-byte encodings and UTF-8;</li>
    <li>And many other <a href="http://sphinxsearch.com/docs/current.html">Sphinx features</a></li>
</ul>

<h4>Requirements:</h4>
<ul>
    <li>Sphinx Search 0.9.9 or higher</li>
    <li>Joomla 1.5</li>
</ul>
